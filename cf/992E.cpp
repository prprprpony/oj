ll qry(int i)
{
    ll r = 0;
    for (; i; i ^= i & -i)
        r += fen[i];
    return r;
}
int bs(ll x)
{
    int k = 0;
    for (int i = 1<<__lg(N); i; i>>=1)
        if ((k | i) < N && fen[k | i] < x)
            x -= fen[k |= i];
    return k + 1;
}
